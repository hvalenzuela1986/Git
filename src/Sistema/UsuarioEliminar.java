    private UsuarioDAO usuario;
    
    public UsuarioEliminar() {
        super();
        usuario = new UsuarioDAO();
        LlenarTabla();
        ModificarTitulos("Usuario");
    }
    
    @Override
    public void LlenarTabla(){
        DefaultTableModel modelo = getModel();
        ArrayList<Usuario> listado = new ArrayList<>();
        modelo.setColumnIdentifiers(new String[]{"RUT", "Nombres", "Apellidos", "Fecha de Creación"});
        for (int i = modelo.getRowCount() - 1; i >= 0; i--) {
            modelo.removeRow(i);
        }
        usuario.ObtenerUsuarios(listado);
        String [] nombres;
        for (int i = 0; i < listado.size(); i++) {
            nombres = listado.get(i).getNombre().split("_");
            modelo.addRow(new Object[]{listado.get(i).getRut(), nombres[0], nombres[1], listado.get(i).getFechaCreacion()});
        }
    }
    
    @Override
    public void Eliminar(){
        int fila = getNumeroFila();
        if(fila != -1){
            String rut = getValor(fila, 0);
            int respuesta = JOptionPane.showConfirmDialog(this, "¿Seguro de eliminar usuario RUT = " + rut + "?", "Confirmación", JOptionPane.YES_NO_OPTION);
            if(respuesta == JOptionPane.YES_OPTION){
                if(usuario.EliminarUsuario(rut)){
                    JOptionPane.showMessageDialog(this, "Usuario eliminado exitosamente");
                    LlenarTabla();
                }
                else{
                    JOptionPane.showMessageDialog(this, "Error al intentar eliminar usuario");
                }
            }
        }
    }

