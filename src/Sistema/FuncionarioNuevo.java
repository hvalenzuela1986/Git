    private void butGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butGuardarActionPerformed
        String rut = txtRut.getText();
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String fechaNac = txtFechaNac.getText();
        int codCargo = cboCargo.getSelectedIndex();
        String fechaIng = txtFechaIng.getText();
        int codNivel = cboNivel.getSelectedIndex();
        int estado = 1;
        if(rut.length() == 0){
            JOptionPane.showMessageDialog(this, "Ingrese RUT");
            txtRut.requestFocus();
        }
        else if(!Login.ValidarRut(rut)){
            JOptionPane.showMessageDialog(this, "RUT no v√°lido");
            txtRut.requestFocus();
        }
        else if(nombre.length() == 0){
            JOptionPane.showMessageDialog(this, "Ingrese Nombre");
            txtNombre.requestFocus();
        }
        else if(apellido.length() == 0){
            JOptionPane.showMessageDialog(this, "Ingrese Apellido");
            txtApellido.requestFocus();
        }
        else if(fechaNac.length() == 0){
            JOptionPane.showMessageDialog(this, "Ingrese Fecha Nacimiento");
            txtFechaNac.requestFocus();
        }
        else if(codCargo < 1){
            JOptionPane.showMessageDialog(this, "Seleccione Cargo");
            cboCargo.requestFocus();
        }
        else if(fechaIng.length() == 0){
            JOptionPane.showMessageDialog(this, "Ingrese Fecha Ingreso");
            txtFechaIng.requestFocus();
        }
        else{
            Funcionario nuevo = new Funcionario(rut, nombre + "_" + apellido, fechaNac, codCargo, fechaIng, codNivel, estado);
            if(funcionario.Guardar(nuevo)){
                JOptionPane.showMessageDialog(this, "Funcionario registrado exitosamente");
                Inicializar();
            }
            else{
                JOptionPane.showMessageDialog(this, "Error al intentar guardar funcionario");
            }
        }
    }//GEN-LAST:event_butGuardarActionPerformed

