    private void ObtenerListados(){
        cboCargo.removeAllItems();
        cboNivel.removeAllItems();
        cboCargo.addItem("<Seleccione Cargo>");
        cboNivel.addItem("<Seleccione Nivel>");
        ArrayList<String> cargos = new ArrayList<>();
        ArrayList<String> niveles = new ArrayList<>();
        cargo.ObtenerCargos(cargos);
        nivel.ObtenerNiveles(niveles);
        for (int i = 0; i < cargos.size(); i++) {
            cboCargo.addItem(cargos.get(i));
        }
        for (int i = 0; i < niveles.size(); i++) {
            cboNivel.addItem(niveles.get(i));
        }
    }

